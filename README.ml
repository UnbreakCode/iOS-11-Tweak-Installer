Open Tweak Installer.exe, change your ssh details, select a deb and either install or uninstall

Or

Usage:
	tic.exe [install] [uninstall] deb
	
If for any reason uninstalling a tweak doesn't work please send me a copy of the script.sh file my program generated so I can look into it. You need to change your devices IP, and root password in the file 'settings'

Example: tic cylinder.deb 
The program will automatically convert to electra format (if you're running Electra (thanks /u/AlexDominat for suggesting this)) and will install / uninstall your deb file. It'll then respring your device automatically.